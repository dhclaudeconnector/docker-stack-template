#!/usr/bin/env node
// ================================================================
//  scripts/generate-cf-config.js
//  Auto-generates cloudflared/config.yml from .env values.
//
//  Required env: PROJECT_NAME, DOMAIN, ENABLE_* flags
//  Output: cloudflared/config.yml (overwrites!)
// ================================================================
'use strict';

const fs   = require('fs');
const path = require('path');

function parseEnvFile(p) {
  if (!fs.existsSync(p)) return {};
  const env = {};
  for (const line of fs.readFileSync(p, 'utf-8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq === -1) continue;
    env[t.slice(0, eq).trim()] = t.slice(eq + 1).trim().replace(/^["']|["']$/g, '');
  }
  return env;
}

const env = parseEnvFile(path.resolve(process.cwd(), '.env'));

const PROJECT_NAME = env.PROJECT_NAME;
const DOMAIN       = env.DOMAIN;

if (!PROJECT_NAME || !DOMAIN) {
  console.error('❌  PROJECT_NAME and DOMAIN must be set in .env');
  process.exit(1);
}

// Read tunnel ID from existing config if available
let tunnelId = '<YOUR-TUNNEL-ID>';
const existing = path.resolve(process.cwd(), 'cloudflared/config.yml');
if (fs.existsSync(existing)) {
  const m = fs.readFileSync(existing, 'utf-8').match(/^tunnel:\s*(.+)$/m);
  if (m) tunnelId = m[1].trim();
}

// Build ingress rules
const rules = [
  `  # Main application`,
  `  - hostname: ${PROJECT_NAME}.${DOMAIN}`,
  `    service: http://caddy:80`,
];

if (env.ENABLE_DOZZLE !== 'false') {
  rules.push(``, `  # Dozzle — container logs`);
  rules.push(`  - hostname: logs.${PROJECT_NAME}.${DOMAIN}`);
  rules.push(`    service: http://caddy:80`);
}
if (env.ENABLE_FILEBROWSER !== 'false') {
  rules.push(``, `  # Filebrowser — file manager`);
  rules.push(`  - hostname: files.${PROJECT_NAME}.${DOMAIN}`);
  rules.push(`    service: http://caddy:80`);
}
if (env.ENABLE_WEBSSH !== 'false') {
  rules.push(``, `  # WebSSH — browser terminal`);
  rules.push(`  - hostname: ttyd.${PROJECT_NAME}.${DOMAIN}`);
  rules.push(`    service: http://caddy:80`);
}

rules.push(``, `  # Catch-all — return 404 for unlisted hostnames`);
rules.push(`  - service: http_status:404`);

const output = `# ================================================================
#  cloudflared/config.yml
#  Auto-generated by: node scripts/generate-cf-config.js
#  Generated at: ${new Date().toISOString()}
#
#  ⚠️  Do NOT commit cloudflared-credentials.json to git!
# ================================================================

tunnel: ${tunnelId}
credentials-file: /etc/cloudflared/credentials.json

ingress:
${rules.join('\n')}
`;

const outPath = path.resolve(process.cwd(), 'cloudflared/config.yml');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, output, 'utf-8');

console.log(`\n✅  Generated ${outPath}`);
console.log(`    Tunnel ID : ${tunnelId}`);
console.log(`    Hostnames :`);
[
  `${PROJECT_NAME}.${DOMAIN}`,
  env.ENABLE_DOZZLE      !== 'false' ? `logs.${PROJECT_NAME}.${DOMAIN}`  : null,
  env.ENABLE_FILEBROWSER !== 'false' ? `files.${PROJECT_NAME}.${DOMAIN}` : null,
  env.ENABLE_WEBSSH      !== 'false' ? `ttyd.${PROJECT_NAME}.${DOMAIN}`  : null,
].filter(Boolean).forEach(h => console.log(`      • ${h}`));

if (tunnelId === '<YOUR-TUNNEL-ID>') {
  console.log(`\n⚠️   Replace "<YOUR-TUNNEL-ID>" in cloudflared/config.yml with your real tunnel ID.`);
  console.log(`    Get it from: cloudflared tunnel list  or Cloudflare Zero Trust dashboard.\n`);
}
